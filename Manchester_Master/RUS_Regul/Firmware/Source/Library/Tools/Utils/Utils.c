// Побайтово поменЯть тетрады в байтах
uint32_t UInt32_SwapTetrades( uint32_t Source )
{
	uint32_t Result = __ROR( Source, 4 );
	Result ^= Source;
	Source ^= Result;
	Result ^= Source;
	return Result;
}

