uint8_t Round_UI8( const float Val )
{
	uint8_t Result;
	if( Val < 0 )
		Result = 0;
	else if( Val > ( float ) UINT8_MAX )
		Result = UINT8_MAX;
	else
		Result = ( uint8_t ) ( Val + 0.5f );
	return Result;
}

