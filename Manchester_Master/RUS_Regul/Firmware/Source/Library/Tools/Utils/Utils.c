// CRC16_SKLP
uint16_t CalcCRC16SKLP_Load( uint8_t *pData, uint32_t Size, uint16_t CRC16 )
{
	uint8_t h_12, h_1, hh_5, hl_5;
	uint16_t temp;

	while( Size-- )
	{
		h_12 = CRC16 >> 8;
		h_1 = h_12;
		h_12 = (h_12 >> 4) | (h_12 << 4);
		hh_5 = h_12;
		h_12 ^= h_1;
		h_12 &= 0xF0;
		hh_5 &= 0x0F;
		h_1 ^= hh_5;
		hl_5 = h_12;
		temp = ((uint16_t)hh_5 << 8) | hl_5;
		temp <<= 1;
		hh_5 = temp >> 8;
		hl_5 = temp;
		h_1 ^= hl_5;
		h_12 ^= hh_5;
		hl_5 = CRC16;
		h_1 ^= *pData++;
		h_12 ^= hl_5;
		CRC16 = (((uint16_t)h_12 << 8) | h_1);
	}

	return CRC16;
}

uint16_t CalcCRC16SKLP( uint8_t *pData, uint32_t Size )
{
	return CalcCRC16SKLP_Load( pData, Size, 0 );
}


// ********* BCD32_t ******************************************************
bool BCD32_to_UInt32( BCD32_t BCD32, uint32_t *pUInt32 )
{
	if( NULL == pUInt32 )
		return false;

	uint32_t UInt32;
	uint8_t i;
	for( i = 0, UInt32 = 0; i < sizeof( BCD32 ) * 2; i++ )
	{
		uint8_t Digit = ( BCD32 & 0xF0000000 ) >> ( 7 * 4 );
		if( Digit > 9 )
			return false;
		UInt32 = UInt32 * 10 + Digit;
		BCD32 <<= 4;
	}

	*pUInt32 = UInt32;
	return true;
}

bool UInt32_to_BCD32( uint32_t UInt32, BCD32_t *pBCD32 )
{
	if( NULL == pBCD32 )
		return false;

	BCD32_t BCD32;
	uint8_t i;
	for( i = 0, BCD32 = 0; i < sizeof( BCD32 ) * 2; i++ )
	{
		BCD32 >>= 4;
		BCD32 |= ( UInt32 % 10 ) << ( 7 * 4 );
		UInt32 /= 10;
	}

	if( 0 != UInt32 )
		return false;

	*pBCD32 = BCD32;
	return true;
}

bool UInt8_to_BCD8( uint8_t UInt8, uint8_t *pBCD8 )
{
	bool Result = false;
	do
	{
		if( NULL == pBCD8 )
			break;
		if( UInt8 / 100 )
			break;
		*pBCD8 = ( ( UInt8 / 10 ) << 4 ) + ( UInt8 % 10 );
		Result = true;
	} while( 0 );
	return Result;
}

uint8_t BCD8_2Bin( uint8_t BCD8 )
{
	uint8_t Low = BCD8 & 0x0F;
	uint8_t High = ( BCD8 >> 4 ) & 0x0F;
	uint8_t Result = 0xFF;
	if( ( Low < 10 ) && ( High < 10 ) )
		Result = Low + 10 * High;
	return Result;
}

